// Cú pháp khai báo biến: từ_khóa_khai báo tên_biến = giá_trị
var myName = "Ngọ Văn Quý"; // Khai báo
// Khai báo các biến liên quan đến: tuổi, ngày sinh, địa chỉ

// Từ khóa var
var myName = "Nguyễn Văn An"; // Khai báo lại // Pascel case: MyName
myName = "Nguyễn Văn Đăng"; // Gán lại giá trị
console.log(myName);

// Từ khóa let => ES6 (2015)
let age = 20;

// let age = 21; // Khai báo lại => Xảy ra lỗi không cho phép khai báo lại
age = 22; // Gán lại giá trị
console.log(age);

// Từ khóa const => ES6 (2015)
const id = "01234567";
// const id = "12345"; // Khai báo lại

const PI = 3.14;

// Kiểu dữ liệu
// 1. string (chuỗi)
let className = "JV232612";
console.log(className);

// 2. number (số)
let year = 2024;
console.log(year);

// 3. boolean: Đúng (true) hoặc Sai (false)
let isMale = false;
console.log(isMale);

// 4. null
let user = null;
console.log(user);

// 5. undefined
let a;
console.log(a);

// 6. NaN (Not a Number)
// "6" -> 6
